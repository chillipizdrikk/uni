def get_valid_mask(Y, X1, X2, X3, B_star, D2,
                   D1=None, D3=None):
    """Маска дійсних спостережень при заданих обмеженнях."""
    mask = np.ones(len(Y), dtype=bool)
    for i in range(3):
        mask &= (Y[:, i] >= B_star[i, 0]) & (Y[:, i] <= B_star[i, 1])
    for j in range(2):
        mask &= (X2[:, j] >= D2[j, 0]) & (X2[:, j] <= D2[j, 1])
    if D1 is not None:
        for j in range(4):
            mask &= (X1[:, j] >= D1[j, 0]) & (X1[:, j] <= D1[j, 1])
    if D3 is not None:
        for j in range(2):
            mask &= (X3[:, j] >= D3[j, 0]) & (X3[:, j] <= D3[j, 1])
    return mask


def empirical_bounds(arr):
    """Обчислити емпіричні межі [min, max] для кожного стовпця."""
    return np.column_stack([arr.min(axis=0), arr.max(axis=0)])


def is_subset(D_inner, D_outer):
    """Перевірити чи D_inner ⊆ D_outer."""
    return (np.all(D_inner[:, 0] >= D_outer[:, 0]) and
            np.all(D_inner[:, 1] <= D_outer[:, 1]))


def correct_bounds_expand(D_pm, D0):
    """
    Коригування меж за Варіантом 14 (розширення):
    x^-_j(new) = d^-_j - Δd^+_j  (якщо D0_min < D^±_min)
    x^+_j(new) = d^+_j + Δd^-_j  (якщо D0_max > D^±_max)

    Δd^+_j = max(0, d^-_j - D0_min_j)
    Δd^-_j = max(0, D0_max_j - d^+_j)
    """
    new_D = D_pm.copy()
    deltas = []
    for j in range(len(D_pm)):
        delta_plus  = max(0.0, D_pm[j, 0] - D0[j, 0])  # D0 виходить знизу
        delta_minus = max(0.0, D0[j, 1] - D_pm[j, 1])  # D0 виходить зверху
        new_D[j, 0] = D_pm[j, 0] - delta_plus
        new_D[j, 1] = D_pm[j, 1] + delta_minus
        deltas.append((delta_plus, delta_minus))
    return new_D, deltas


