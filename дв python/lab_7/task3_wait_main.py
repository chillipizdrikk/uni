#  - Демонстрація режиму "Очікування закінчення дочірнього процесу".
#  - Основний процес записує початкові дані у спільний файл comdata.txt,
#    потім запускає дочірній процес через subprocess.run(),
#    дочірній процес модифікує дані і записує назад у файл.
#  - Після завершення дочірнього процесу основний читає файл, модифікує ще раз і виводить результати.
#
# Примітка:
#  - Запускайте цей скрипт в консольному режимі (щоб побачити input() паузу),
#    або через середовище, яке показує stdout.
#
# Самодокументування: є print() перед і після кожної важливої дії.

import subprocess
import os

print("task3_wait_main.py - початок (режим очікування дочірнього процесу)")
fn = "comdata.txt"  # спільний файл даних
ab = [4, 5, 6, 0, -1, -2, -3]  # початковий список значень
print("Початковий список ab:", ab)

# Запис початкових даних у файл
with open(fn, "w") as data:
    data.write(" ".join(map(str, ab)) + "\n")
print(f"Записано початкові дані у файл '{fn}'")

# Побудова шляху до дочірнього скрипта (той же каталог)
dirname = os.path.abspath('.')
child_script = os.path.join(dirname, 'task3_child.py')

# Перевірка наявності дочірнього скрипта
if not os.path.exists(child_script):
    print("НЕ знайдено дочірнього скрипта task3_child.py у поточній папці.")
    print("Створіть файл task3_child.py на основі наданого зразка і запустіть ще раз.")
    exit(1)

print("Запускаю дочірній процес (task3_child.py) і чекаю на його завершення...")
# Запускаємо дочірній процес та чекаємо на завершення
# Використовуємо 'python' — в залежності від системи можливо 'python3' або повний шлях
subprocess.run(["python", child_script])

print("Дочірній процес завершився, читаю оновлені дані з файлу:", fn)
with open(fn, "r") as subdata:
    ef = [int(x) for x in subdata.readline().rstrip().split()]
print("Дані, прочитані після завершення дочірнього процесу:", ef)

# Додаємо 1 до кожного елемента
for k in range(len(ef)):
    ef[k] += 1
print("Після додавання 1 до кожного елементу:", ef)

with open(fn, "w") as data:
    data.write(" ".join(map(str, ef)) + "\n")
print("Оновлені дані записані назад у файл:", fn)

input("Натисніть Enter щоб завершити основний процес...")
print("task3_wait_main.py - кінець сценарію")